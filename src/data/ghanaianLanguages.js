// Helper function to get translated text
export const getTranslation = (key, language = 'en', category = 'phrases') => {
  const translations = ghanaianLanguages[category];
  if (!translations || !translations[language] || !translations[language][key]) {
    // Fallback to English if translation not found
    return translations?.en?.[key] || key;
  }
  return translations[language][key];
};

// Helper function to get language info
export const getLanguageInfo = (langCode) => {
  return ghanaianLanguages.languages[langCode] || ghanaianLanguages.languages.en;
};

// Helper function to get all supported languages
export const getSupportedLanguages = () => {
  return Object.keys(ghanaianLanguages.languages).map(code => ({
    code,
    ...ghanaianLanguages.languages[code]
  }));
};

